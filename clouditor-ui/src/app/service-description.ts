export class ServiceDescription {
  components: ServiceComponent[];

  constructor(components: Object, public id: string) {
    this.components = this.toNodes(components);
  }

  public toNodes(object: Object): ServiceComponent[] {
    const nodes = [];

    for (const name of Object.keys(object)) {
      nodes.push(object[name]);
    }

    return nodes;
  }
}

export class ServiceComponent {
  constructor(public type: string,
    public name: string,
    public id: string,
    public aliases: string[],
    public securityStatus: string) { }
}
