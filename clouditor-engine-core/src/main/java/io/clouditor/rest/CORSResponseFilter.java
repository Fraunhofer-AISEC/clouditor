package io.clouditor.rest;

import io.clouditor.Component;
import javax.annotation.Priority;
import javax.inject.Inject;
import javax.ws.rs.Priorities;
import javax.ws.rs.container.ContainerRequestContext;
import javax.ws.rs.container.ContainerResponseContext;
import javax.ws.rs.container.ContainerResponseFilter;
import javax.ws.rs.core.MultivaluedMap;

/**
 * This filter adds Cross-Origin Resource Sharing (CORS) headers to each response.
 *
 * @author Christian Banse
 */
@Priority(Priorities.HEADER_DECORATOR)
public class CORSResponseFilter implements ContainerResponseFilter {

  @Inject private Component component;

  @Override
  public void filter(
      ContainerRequestContext requestContext, ContainerResponseContext responseContext) {

    MultivaluedMap<String, Object> headers = responseContext.getHeaders();

    // allow AJAX from everywhere
    headers.add("Access-Control-Allow-Origin", component.getAPIAllowedOrigin());
    headers.add("Access-Control-Allow-Methods", "GET,PUT,POST,DELETE");
    headers.add("Access-Control-Allow-Headers", "Authorization,Content-Type");
  }
}
