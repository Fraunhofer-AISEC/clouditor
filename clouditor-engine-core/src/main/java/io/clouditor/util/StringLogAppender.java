package io.clouditor.util;

import java.io.Serializable;
import java.time.Instant;
import java.time.ZoneOffset;
import java.time.format.DateTimeFormatter;
import java.util.ArrayList;
import java.util.List;
import org.apache.logging.log4j.core.Filter;
import org.apache.logging.log4j.core.Layout;
import org.apache.logging.log4j.core.LogEvent;
import org.apache.logging.log4j.core.appender.AbstractAppender;

public class StringLogAppender extends AbstractAppender {

  private List<String> lines = new ArrayList<>();

  public StringLogAppender(String name, Filter filter, Layout<? extends Serializable> layout) {
    super(name, filter, layout);
  }

  @Override
  public void append(LogEvent event) {
    lines.add(
        DateTimeFormatter.ISO_LOCAL_DATE_TIME
                .format(Instant.ofEpochMilli(event.getTimeMillis()).atOffset(ZoneOffset.UTC))
                .replace("T", " ")
            + " "
            + event.getLevel()
            + " "
            + event.getMessage().getFormattedMessage());
  }

  public List<String> getLines() {
    return lines;
  }
}
