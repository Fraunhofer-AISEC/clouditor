package io.clouditor.assurance.ccl;

import java.util.ArrayList;
import java.util.List;
import java.util.Map;
import java.util.stream.Collectors;

public class WithinExpression extends Expression {

  private String field;

  private List<Value> values = new ArrayList<>();

  @Override
  public boolean evaluate(Map asset) {
    // TODO: probably assert, whether they are all the same type?!
    var fieldValue = getValueFromField(asset, this.field);

    var list = values.stream().map(Value::getValue).collect(Collectors.toList());

    return list.contains(fieldValue);
  }

  public String getField() {
    return field;
  }

  public void setField(String field) {
    this.field = field;
  }

  public void setValues(List<Value> values) {
    this.values = values;
  }
}
