package io.clouditor.util;

import static org.junit.jupiter.api.Assertions.assertNotNull;
import static org.junit.jupiter.api.Assertions.assertThrows;

import java.io.IOException;
import org.junit.jupiter.api.Test;

class FileSystemManagerTest {

  @Test
  void test() throws IOException {
    // make sure, that the JAR file system exists
    var p = FileSystemManager.getInstance().getPathForResource("LICENSE.md");

    assertNotNull(p);

    // make sure, that no JAR file system objects exists
    FileSystemManager.getInstance().cleanup();

    // try cleanup if it does not exist
    FileSystemManager.getInstance().cleanup();
  }

  @Test
  void testNonExistingPrivateKey() {
    assertThrows(
        IOException.class, () -> FileSystemManager.getInstance().getPrivateKey("doesnotexist.pem"));
  }
}
