package io.clouditor;

import org.junit.jupiter.api.AfterEach;
import org.junit.jupiter.api.BeforeEach;

public abstract class AbstractEngineUnitTest {

  protected Engine engine;

  @BeforeEach
  protected void setUp() {
    this.engine = new Engine();

    // set in-memory db for testing
    this.engine.setDbInMemory(true);

    // set the API port to 0 (first possible) to avoid possible conflicts
    this.engine.setAPIPort(0);
  }

  @AfterEach
  protected void cleanUp() {
    // make sure, proxy is reset after each test
    System.setProperty("http.proxy", "");
    System.setProperty("https.proxy", "");
  }
}
