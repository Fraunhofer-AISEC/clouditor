package io.clouditor.credentials;

import static org.junit.jupiter.api.Assertions.assertEquals;
import static org.junit.jupiter.api.Assertions.assertNotNull;

import com.microsoft.azure.credentials.ApplicationTokenCredentials;
import java.io.IOException;
import org.junit.jupiter.api.Test;

class AzureAccountTest {

  @Test
  void testResolveCredentials() throws IOException {
    var account = new AzureAccount();
    account.setClientId("my-client-id");
    account.setClientSecret("secret");
    account.setTenantId("my-tenant-id");
    account.setAutoDiscovered(false);

    var credentials = account.resolveCredentials();

    assertNotNull(credentials);

    assertEquals("my-client-id", ((ApplicationTokenCredentials) credentials).clientId());
  }
}
